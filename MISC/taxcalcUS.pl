my $ifh = util_read($infile);


my $info = {};
my $totE = 0 ;
my $totEMinusTax = 0 ;
while(<$ifh>){
	
	my @l = split "," , $_ ;
	$totEMinusTax += $l[3] ;
	$totE += $l[2] ;
}

my $TAXPAID = $totE - $totEMinusTax ;
print "$TAXPAID = $totE - $totEMinusTax \n";
chmod 0777, $outfile ;

my $lowslab = 9225 ;
my $DELTA = $totE - $lowslab ;

my $XPERCET = (15 * $DELTA)/100 ;

my $TOT2PAY = $XPERCET + $lowslab/10 ;
print "TOT2PAY $TOT2PAY = $XPERCET + $lowslab/10 ; \n";


sub usage{
