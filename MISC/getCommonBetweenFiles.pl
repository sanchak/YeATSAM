           );
die "Dont recognize command line arg @ARGV " if(@ARGV);
usage( "Need to give a output file name => option -outfile ") if(!defined $outfile);
usage( "Need to give a listfile -option -listfile  ") if(!defined $listfile);

my @list= util_read_list_sentences($listfile);
my $list = {};
map { s/\s*//g ; $list->{$_} = 1 ; } @list ;

system("newfile.csh $outfile");
while(@list){
	my $A = shift @list ;
	foreach my $B (@list){
		system("twolists.pl $A $B");
		system("cat ofhboth.1 >> $outfile");
	}
}


if(defined $hackforHISTONE){
    my $IFH = util_read($outfile);
    my $OFH = util_write("ignorepdbs");
    my @l ;
    while(<$IFH>){
        chomp ;
	    s/.$//;
	    push @l, $_ ;
    }
    my $str = join "|", @l;
	$str = $str . "|" . $hackforHISTONE ;
    print $OFH "$str \n";
}



